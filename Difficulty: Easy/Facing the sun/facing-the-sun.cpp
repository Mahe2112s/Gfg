class Solution {
  public:
    // Returns count buildings that can see sunlight
    int countBuildings(vector<int> &height) {
        // code here
        int c=1,j=1,i=0,temp=height[i];
        while(j<height.size())
        {
            if(height[j]>height[i] and height[j]>height[j-1] and height[j]>temp){
                c+=1;
                temp=height[j];
            }
            
            j++;
        }
        return c;
    }
};

