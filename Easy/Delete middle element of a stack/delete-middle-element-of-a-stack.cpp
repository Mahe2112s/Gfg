class Solution
{
    public:
    //Function to delete middle element of a stack.
    void deleteMid(stack<int>&s, int size,int temp)
    {
        // code here.. 
        if(temp==size/2)
        {
            s.pop();
            return ;
        }
        int a=s.top();
        s.pop();
        deleteMid(s,size,temp+=1);
        s.push(a);
    }
};

